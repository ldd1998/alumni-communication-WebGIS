package com.entity;

public class News {
    private String newsId;
    private String newsTitle;
    private String newsContent;
    private String newsUserid;
    private String newsImage;
    private String newsTime;
    private String newsImageTitle;

    public String getNewsId() {
        return newsId;
    }

    public void setNewsId(String newsId) {
        this.newsId = newsId;
    }

    public String getNewsTitle() {
        return newsTitle;
    }

    public void setNewsTitle(String newsTitle) {
        this.newsTitle = newsTitle;
    }

    public String getNewsContent() {
        return newsContent;
    }

    public void setNewsContent(String newsContent) {
        this.newsContent = newsContent;
    }

    public String getNewsUserid() {
        return newsUserid;
    }

    public void setNewsUserid(String newsUserid) {
        this.newsUserid = newsUserid;
    }

    public String getNewsImage() {
        return newsImage;
    }

    public void setNewsImage(String newsImage) {
        this.newsImage = newsImage;
    }

    public String getNewsTime() {
        return newsTime;
    }

    public void setNewsTime(String newsTime) {
        this.newsTime = newsTime;
    }

    public String getNewsImageTitle() {
        return newsImageTitle;
    }

    public void setNewsImageTitle(String newsImageTitle) {
        this.newsImageTitle = newsImageTitle;
    }
}
